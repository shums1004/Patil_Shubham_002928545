    public void populateComboBox() {
        
        for( int i=0; i < directory.getDirectory().size(); i++){
             Person pr = new Person();
             pr = directory.getDirectory().get(i);
             String ID = String.valueOf(pr.getId());
             cmbBoxName.addItem(ID);

          
        }
    }

