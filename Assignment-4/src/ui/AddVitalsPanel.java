    private void btnSaveVitalsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveVitalsActionPerformed
        // TODO add your handling code here:
        
        int HeartRate = Integer.parseInt(txtHeartRate.getText());
        double Dystolic = Double.parseDouble(txtDystolic.getText());
        double Systolic = Double.parseDouble(txtSystolic.getText());
        int BloodOxygen = Integer.parseInt(txtBloodOxygenLevel.getText());
        double Glucose = Double.parseDouble(txtGlucoseLevel.getText());
        double Cholestrol = Double.parseDouble(txtCholestrolLevel.getText());
        
        Vitals vr = vdirectory.addNewVitals();
        ehistory.AddEncounter(Integer.parseInt(cmbBoxName.getSelectedItem().toString()), vr);
        
        vr.setHeartRate(HeartRate);
        vr.setDystolic(Dystolic);
        vr.setSystolic(Systolic);
        vr.setBloodOxygen(BloodOxygen);
        vr.setGlucose(Glucose);
        vr.setCholestrol(Cholestrol);
        vr.setTimeStamp();
        JOptionPane.showMessageDialog(this, "Vitals have been saved");
        
    }//GEN-LAST:event_btnSaveVitalsActionPerformed

