    public void addDetails(){
        
        String Name = txtName.getText();
        int Id = Integer.parseInt(txtId.getText());
        int Age = Integer.parseInt(txtAge.getText());
        String Phone = txtPhone.getText();
        double Height = Double.parseDouble(txtHeight.getText());
        double Weight = Double.parseDouble(txtWeight.getText());
        int House_No = Integer.parseInt(txtHouseNo.getText());
        String Community_Name = txtCommunity.getText();
        String City = txtCity.getText();
        
        cmbBoxUpdate.addItem(String.valueOf(Id));
        Person pr = directory.addNewPersons();
        
        pr.setName(Name);
        pr.setId(Id);
        pr.setAge(Age);
        pr.setPhone(Phone);
        pr.setHeight(Height);
        pr.setWeight(Weight);
        pr.setHouse_No(House_No);
        pr.setCommunity_Name(Community_Name);
        pr.setCity_name(City);
    }
