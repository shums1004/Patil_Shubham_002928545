    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        for (int i=0; i<directory.getDirectory().size(); i++) {
            
             Person pr = new Person();
             pr = directory.getDirectory().get(i);
            String ID1 = String.valueOf(pr.getId());
        
                if(ID1.equals(txtId.getText())){
                    JOptionPane.showMessageDialog(this, "This Id is already taken. Please select a new Id");
                    
                    return;
                }
        }
        
        if(txtName.getText().equals("")){
           
            JOptionPane.showMessageDialog(this, "Please Enter your Name");  
            
        
        }
         else if(txtId.getText().equals("")){
           
            JOptionPane.showMessageDialog(this, "Please Enter your Id");  
            
         }
         else if(txtAge.getText().equals("")){
           
            JOptionPane.showMessageDialog(this, "Please Enter your Age");  
            
         }
         
         else if(Integer.parseInt(txtAge.getText())> 200 ||Integer.parseInt(txtAge.getText())<0 ){
         
            JOptionPane.showMessageDialog(this, "Please enter a valid age");    
        }
         
         else if(txtPhone.getText().equals("")){
           
            JOptionPane.showMessageDialog(this, "Please Enter your Phone Number"); 
            
            
         }
         
         else if(Long.parseLong(txtPhone.getText()) < 99999999 ){
            
            JOptionPane.showMessageDialog(this, "Please enter a valid Phone Number");    
        }
         
         
         else if(txtHeight.getText().equals("")){
           
            JOptionPane.showMessageDialog(this, "Please Enter your Height");  
            
         }
         
         else if(Integer.parseInt(txtHeight.getText()) > 400 || Integer.parseInt(txtHeight.getText()) < 1 ){
            
            JOptionPane.showMessageDialog(this, "Are you an Alien. Please enter correct Height");    
        }
         
         else if(txtWeight.getText().equals("")){
           
            JOptionPane.showMessageDialog(this, "Please Enter your Weight");  
            
         }
          else if(Integer.parseInt(txtWeight.getText()) > 400 || Integer.parseInt(txtWeight.getText()) < 1 ||txtWeight.getText().equals("")){
            
            JOptionPane.showMessageDialog(this, "Are you an Alien. Please enter correct Weight");    
        }
       
         else if(txtHouseNo.getText().equals("")){
           
            JOptionPane.showMessageDialog(this, "Please Enter your House Number ");  
            
         }
          else if(txtCommunity.getText().equals("")){
           
            JOptionPane.showMessageDialog(this, "Please Enter Your Community Name");   
        
        }
        
       else if(txtCity.getText().equals("")){
           
            JOptionPane.showMessageDialog(this, "Please Enter your city of residence");   
        
        }
        
       else{
        addDetails();
        JOptionPane.showMessageDialog(this, "Personal details have been saved");
        
        
         emptyfields();
       }
         //populateComboBoxUpdate();
    }//GEN-LAST:event_btnSaveActionPerformed

    
